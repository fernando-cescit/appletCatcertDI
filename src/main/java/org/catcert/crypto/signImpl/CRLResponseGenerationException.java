package org.catcert.crypto.signImpl;

public class CRLResponseGenerationException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public CRLResponseGenerationException() {
		super();
	}

	public CRLResponseGenerationException(String message) {
		super(message);
	}

	public CRLResponseGenerationException(Throwable cause) {
		super(cause);
	}

	public CRLResponseGenerationException(String message, Throwable cause) {
		super(message, cause);
	}

}
