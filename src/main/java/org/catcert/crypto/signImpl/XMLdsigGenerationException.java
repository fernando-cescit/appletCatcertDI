package org.catcert.crypto.signImpl;

public class XMLdsigGenerationException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 3972163273560655255L;

	public XMLdsigGenerationException() {
		super();
	}

	public XMLdsigGenerationException(String message) {
		super(message);
	}

	public XMLdsigGenerationException(String message, Throwable cause) {
		super(message, cause);
	}

	public XMLdsigGenerationException(Throwable cause) {
		super(cause);
	}

}
