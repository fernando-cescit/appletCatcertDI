package org.catcert.crypto.signImpl;

public class TimeStampGenerationException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -1487859193582718217L;

	public TimeStampGenerationException() {
		super();
	}

	public TimeStampGenerationException(String message) {
		super(message);
	}

	public TimeStampGenerationException(String message, Throwable cause) {
		super(message, cause);
	}

	public TimeStampGenerationException(Throwable cause) {
		super(cause);
	}

}
