package org.catcert.crypto.signImpl;

public class PDFSignatureGenerationException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -845400796535676801L;

	public PDFSignatureGenerationException() {
		super();
	}

	public PDFSignatureGenerationException(String message) {
		super(message);
	}

	public PDFSignatureGenerationException(String message, Throwable cause) {
		super(message, cause);
	}

	public PDFSignatureGenerationException(Throwable cause) {
		super(cause);
	}

}
