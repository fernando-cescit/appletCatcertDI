package org.catcert.crypto.keyStoreImpl;

public class JavaKeyStoreException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -6135494577055198720L;

	public JavaKeyStoreException() {
		super();
	}

	public JavaKeyStoreException(String message) {
		super(message);
	}

	public JavaKeyStoreException(String message, Throwable cause) {
		super(message, cause);
	}

	public JavaKeyStoreException(Throwable cause) {
		super(cause);
	}

}
